#include "config.h"

#include <stdlib.h>
#include <string.h>

void
free_config(struct config *config)
{
    if (config->bind_address != NULL)
        freeaddrinfo(config->bind_address);

    if (config->password != NULL)
        free(config->password);

    memset(config, 0, sizeof(*config));
}
